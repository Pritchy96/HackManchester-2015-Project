package com.policedata.objects;

public class Person
{
  private String bio;

  private String name;

  private String rank;

public String getBio()
{
	return bio;
}

public void setBio(String bio)
{
	this.bio = bio;
}

public String getName()
{
	return name;
}

public void setName(String name)
{
	this.name = name;
}

public String getRank()
{
	return rank;
}

public void setRank(String rank)
{
	this.rank = rank;
}
}
